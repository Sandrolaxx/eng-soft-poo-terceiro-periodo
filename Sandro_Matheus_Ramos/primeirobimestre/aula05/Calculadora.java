package Sandro_Matheus_Ramos.aula05;

public class Calculadora {

    double valorUm;

    double valorDois;

    void somar() {
        System.out.println(valorUm + valorDois);
    }

    void subtrair() {
        System.out.println(valorUm - valorDois);
    }

    void multiplicar() {
        System.out.println(valorUm * valorDois);
    }

    void dividir() {
        System.out.println(valorUm + valorDois);
    }

}
