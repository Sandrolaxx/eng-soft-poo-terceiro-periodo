package aula05;

public class Calculadora {
    
    double valorUm;
    double valorDois;

    void somar(){
        System.out.println(valorDois + valorUm);
    }
    
    void subtrair(){
        System.out.println(valorDois - valorUm);
    }
    
    void multiplicar(){
        System.out.println(valorDois * valorUm);
    }
        
    void divisao(){
        System.out.println(valorDois / valorUm);
    }
}
